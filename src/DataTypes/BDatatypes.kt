package DataTypes

fun main(){

    //Int TO long
        val intValue: Int = 100
        val longValue: Long = intValue.toLong()
        val doubleValue: Double = intValue.toDouble()
        val floatValue: Float = intValue.toFloat()
        val stringValue: String = intValue.toString()
    //Output
        println(longValue)
        println(doubleValue)
        println(floatValue)
        println(stringValue)

}
