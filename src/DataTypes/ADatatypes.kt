package DataTypes

// val → Immutable (read-only)
// var → Mutable (can change)
fun main(){

    val Byte: Byte = 10
    val Short: Short = 5
    val Int: Int = 2
    val Long: Long = 1
    val Float: Float = 3.14f
    val Double: Double =  3.14
    val Char: Char = 'M'
    val isActive: Boolean = true
    var String: String = "Hello Datatypes"

    //Out put for KOTLIN
    println(isActive)
    println(Char)
    println(Int)
    println(Long)





}